//! Shared shutdown-signal handling for long-running CLI commands.

/// Resolve when an interrupt (Ctrl+C) or termination signal arrives.
///
/// On Unix this waits for either SIGINT or SIGTERM; elsewhere it waits for
/// Ctrl+C only.
pub async fn shutdown_signal() {
    #[cfg(unix)]
    {
        use tokio::signal::unix::{signal, SignalKind};
        let mut term = match signal(SignalKind::terminate()) {
            Ok(s) => s,
            Err(_) => {
                let _ = tokio::signal::ctrl_c().await;
                return;
            }
        };
        tokio::select! {
            _ = tokio::signal::ctrl_c() => {},
            _ = term.recv() => {},
        }
    }
    #[cfg(not(unix))]
    {
        let _ = tokio::signal::ctrl_c().await;
    }
}
